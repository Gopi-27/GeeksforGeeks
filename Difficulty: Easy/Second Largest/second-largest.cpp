// User function template for C++
class Solution {
  public:
    // Function returns the second
    // largest elements
    int getSecondLargest(vector<int> &arr) {
        // Code Here
        int fir_max=-1;
        for(int b : arr){
            if(b>fir_max)fir_max=b;
        }
        int sec_max=-1;
        for(int b : arr){
            if(b>sec_max && b<fir_max)sec_max=b;
        }
        return sec_max;
    }
};


